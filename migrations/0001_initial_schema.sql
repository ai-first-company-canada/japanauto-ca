-- ============================================================================
-- TRIGGERS: rolling 10-year age cap on listings.year (ADR-0007)
-- ============================================================================
-- Implemented as triggers because SQLite forbids non-deterministic strftime()
-- in CHECK constraints. The rolling window shifts automatically with system
-- clock — no annual schema migration needed.
--
-- Lower bound: currentYear - 10  (e.g. 2026 → year >= 2016)
-- Upper bound: currentYear + 1   (early model-year release allowance)
--
-- Application layer (zod) duplicates this rule for friendly 422 errors;
-- the trigger is the last line of defence at DB level.
-- ============================================================================
CREATE TRIGGER trg_listings_age_cap_insert
BEFORE INSERT ON listings
BEGIN
  SELECT RAISE(ABORT, 'listings.year out of rolling window (currentYear-10 .. currentYear+1)')
  WHERE NEW.year < CAST(strftime('%Y','now') AS INTEGER) - 10
     OR NEW.year > CAST(strftime('%Y','now') AS INTEGER) + 1;
END;

CREATE TRIGGER trg_listings_age_cap_update
BEFORE UPDATE OF year ON listings
BEGIN
  SELECT RAISE(ABORT, 'listings.year out of rolling window (currentYear-10 .. currentYear+1)')
  WHERE NEW.year < CAST(strftime('%Y','now') AS INTEGER) - 10
     OR NEW.year > CAST(strftime('%Y','now') AS INTEGER) + 1;
END;

-- featured_slots table is intentionally exempt: it advertises NEW vehicles
-- (model_year >= currentYear), which by definition fall outside the used-cap.

-- ============================================================================
-- TRIGGERS: keep updated_at fresh on row modifications
-- ============================================================================
CREATE TRIGGER trg_dealers_updated_at
AFTER UPDATE ON dealers FOR EACH ROW
BEGIN
  UPDATE dealers SET updated_at = CAST(strftime('%s','now') AS INTEGER) WHERE id = NEW.id AND updated_at = OLD.updated_at;
END;

CREATE TRIGGER trg_listings_updated_at
AFTER UPDATE ON listings FOR EACH ROW
BEGIN
  UPDATE listings SET updated_at = CAST(strftime('%s','now') AS INTEGER) WHERE id = NEW.id AND updated_at = OLD.updated_at;
END;

CREATE TRIGGER trg_parts_updated_at
AFTER UPDATE ON parts FOR EACH ROW
BEGIN
  UPDATE parts SET updated_at = CAST(strftime('%s','now') AS INTEGER) WHERE id = NEW.id AND updated_at = OLD.updated_at;
END;

CREATE TRIGGER trg_featured_slots_updated_at
AFTER UPDATE ON featured_slots FOR EACH ROW
BEGIN
  UPDATE featured_slots SET updated_at = CAST(strftime('%s','now') AS INTEGER) WHERE id = NEW.id AND updated_at = OLD.updated_at;
END;
