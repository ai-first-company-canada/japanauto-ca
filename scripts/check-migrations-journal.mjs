/**
 * scripts/check-migrations-journal.mjs (deep-audit OPS-3, WS-4)
 *
 * Compares migrations/*.sql against the prod D1 migration journal
 * (d1_migrations) and fails on confirmed drift — this DB has a documented
 * history of out-of-band applies. Wired into predeploy (blocking: a confirmed
 * drift means the code you're about to ship may disagree with the prod
 * schema) and into deploy.yml as a warning step (a drift must not kill the
 * 3-hourly catalog redeploy).
 *
 * Modes:
 *   node scripts/check-migrations-journal.mjs             # check (exit 1 on drift)
 *   node scripts/check-migrations-journal.mjs --snapshot  # also rewrite migrations/JOURNAL.md
 *
 * Exit codes: 0 = in sync (or D1 unreachable → WARN + skip, offline-friendly);
 *             1 = confirmed drift (unapplied and/or phantom migrations listed).
 *
 * Auth: same as export-catalog-data.mjs — local `wrangler login` or
 * CLOUDFLARE_API_TOKEN/CLOUDFLARE_ACCOUNT_ID in CI (D1:read suffices).
 */

import { execFileSync } from "node:child_process";
import { readdirSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

// Documented out-of-band exceptions (journal rows or files that are KNOWN to
// mismatch and were consciously accepted). Keep this list empty unless a
// runbook-documented case forces an entry.
const KNOWN_EXCEPTIONS = [];

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const snapshot = process.argv.includes("--snapshot");

function localMigrations() {
  return readdirSync(join(root, "migrations"))
    .filter((f) => /^\d{4}_.+\.sql$/.test(f))   // migrations/dev/ never matches (readdir is non-recursive)
    .sort();
}

function remoteJournal() {
  const out = execFileSync("npx", [
    "wrangler", "d1", "execute", "japanauto-prod", "--remote", "--json",
    "--command", "SELECT id, name, applied_at FROM d1_migrations ORDER BY id",
  ], { cwd: root, encoding: "utf8", maxBuffer: 16 * 1024 * 1024 });
  return JSON.parse(out)[0]?.results ?? [];
}

const local = localMigrations();
let journal;
try {
  journal = remoteJournal();
} catch (e) {
  console.warn("WARN: journal check skipped (D1 unreachable / no auth):",
    e instanceof Error ? e.message.split("\n")[0] : e);
  process.exit(0);
}

const journalNames = journal.map((r) => r.name);
const localSet = new Set(local);
const journalSet = new Set(journalNames);
const exceptions = new Set(KNOWN_EXCEPTIONS);

const unapplied = local.filter((f) => !journalSet.has(f) && !exceptions.has(f));
const phantom = journalNames.filter((n) => !localSet.has(n) && !exceptions.has(n));

if (snapshot) {
  const lines = [
    "# D1 migration journal — prod snapshot (`japanauto-prod`)",
    "",
    "Generated by `node scripts/check-migrations-journal.mjs --snapshot` — do not edit by hand.",
    `Last refreshed: ${new Date().toISOString()}`,
    "",
    "| id | name | applied_at |",
    "|---:|------|------------|",
    ...journal.map((r) => `| ${r.id} | ${r.name} | ${r.applied_at} |`),
    "",
    "Note: `0006_seed_sample_donor.sql` lives in `migrations/dev/` (dev-only seed)",
    "and is intentionally absent from both this journal and `migrations/*.sql`.",
    "",
  ];
  writeFileSync(join(root, "migrations", "JOURNAL.md"), lines.join("\n"));
  console.log(`journal snapshot: ${journal.length} rows → migrations/JOURNAL.md`);
}

if (unapplied.length === 0 && phantom.length === 0) {
  console.log(`migrations journal: in sync (${local.length} local, ${journal.length} applied)`);
  process.exit(0);
}

if (unapplied.length) {
  console.error(`DRIFT — local migration files NOT in the prod journal (apply or investigate):`);
  for (const f of unapplied) console.error(`  - ${f}`);
}
if (phantom.length) {
  console.error(`DRIFT — journal rows with NO local file (renamed/deleted? backfill per docs/runbook.md):`);
  for (const n of phantom) console.error(`  - ${n}`);
}
process.exit(1);
