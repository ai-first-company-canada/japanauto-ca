/**
 * Postbuild step (audit #18): scan every HTML page under dist/ for executable
 * inline <script> bodies, SHA-256 them, and write the deduplicated hash list
 * to functions/_lib/csp-script-hashes.ts. _middleware.ts folds these into the
 * script-src directive so the shared CSP header can drop 'unsafe-inline' —
 * every build-time inline script is then individually allowlisted by hash
 * (runtime Pages Functions HTML uses a per-request nonce instead).
 *
 * Also acts as a regression gate: inline event handler attributes (onclick=,
 * onload=, …) cannot be allowed by hash or nonce (only by 'unsafe-hashes'),
 * so any page that reintroduces one FAILS the build here — silently shipping
 * it would mean dead buttons in production.
 *
 * Run order: after `astro build` (any step that rewrites dist HTML must come
 * first). The output file is committed so `npm run typecheck` works without
 * a prior build; it regenerates on every build.
 */
import { createHash } from 'node:crypto';
import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { join } from 'node:path';

const DIST = 'dist';
const OUT = 'functions/_lib/csp-script-hashes.ts';
// Header-size guard: each hash adds ~52 bytes to every response's CSP header.
// 24 unique scripts today; a blowout usually means a component started
// interpolating per-page values into its inline script — dedupe it instead.
const MAX_HASHES = 64;

function* htmlFiles(dir) {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const p = join(dir, entry.name);
    if (entry.isDirectory()) yield* htmlFiles(p);
    else if (entry.name.endsWith('.html')) yield p;
  }
}

const SCRIPT_RE = /<script\b([^>]*)>([\s\S]*?)<\/script>/gi;
// Tag-scoped so prose mentioning "onclick" can't false-positive; script
// bodies that build markup strings with inline handlers DO match — those
// would be blocked at runtime too (innerHTML'd on*= needs 'unsafe-hashes').
const HANDLER_RE = /<[a-zA-Z][^>]*\son[a-z]+\s*=/;
const EXEC_TYPES = /^(module|text\/javascript|application\/javascript)$/i;

const hashes = new Set();
const handlerHits = [];
let pages = 0;

for (const file of htmlFiles(DIST)) {
  pages++;
  const html = readFileSync(file, 'utf8');
  let m;
  while ((m = SCRIPT_RE.exec(html)) !== null) {
    const [, attrs, body] = m;
    if (/\bsrc\s*=/i.test(attrs)) continue; // external — covered by 'self'
    const type = /\btype\s*=\s*["']([^"']+)["']/i.exec(attrs)?.[1];
    if (type && !EXEC_TYPES.test(type)) continue; // ld+json etc. — not executable
    hashes.add(createHash('sha256').update(body, 'utf8').digest('base64'));
  }
  if (HANDLER_RE.test(html)) handlerHits.push(file);
}

if (pages === 0) {
  console.error('[csp-hashes] no HTML found in dist/ — run astro build first');
  process.exit(1);
}
if (handlerHits.length > 0) {
  console.error(
    `[csp-hashes] FAIL: inline event handler attributes (on*=) found in ${handlerHits.length} page(s).\n` +
    `CSP script-src has no 'unsafe-inline'/'unsafe-hashes', so these handlers are dead in production.\n` +
    `Convert them to addEventListener (see [data-open-modal] delegation in BaseLayout.astro).\n` +
    `First offenders:\n` + handlerHits.slice(0, 10).map((f) => `  - ${f}`).join('\n'),
  );
  process.exit(1);
}
if (hashes.size === 0) {
  console.error('[csp-hashes] no inline scripts found — dist looks broken, refusing to write an empty allowlist');
  process.exit(1);
}
if (hashes.size > MAX_HASHES) {
  console.error(
    `[csp-hashes] FAIL: ${hashes.size} unique inline scripts (cap ${MAX_HASHES}).\n` +
    `A component is likely interpolating per-page values into its inline <script>; ` +
    `move the varying part into a data-* attribute so the script body stays constant.`,
  );
  process.exit(1);
}

const sorted = [...hashes].sort();
const out = `/**
 * AUTO-GENERATED by scripts/generate-csp-hashes.mjs — do not edit by hand.
 * SHA-256 hashes of every unique executable inline <script> body in dist/.
 * Consumed by functions/_middleware.ts to build the script-src directive
 * without 'unsafe-inline' (audit #18). Regenerated on every \`npm run build\`.
 */
export const SCRIPT_HASHES: readonly string[] = [
${sorted.map((h) => `  'sha256-${h}',`).join('\n')}
];
`;
writeFileSync(OUT, out);
console.log(`[csp-hashes] ${pages} pages scanned, ${sorted.length} unique inline scripts → ${OUT}`);
